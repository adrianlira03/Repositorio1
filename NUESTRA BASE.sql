CREATE OR REPLACE FUNCTION JEFE_NIF (NIF_JEFE VARCHAR2) 
RETURN VARCHAR2 IS
CURSOR CEMPLEADO IS
SELECT * FROM JEFE WHERE NIF_JEFE LIKE NIF;
REGISTRO CEMPLEADO%ROWTYPE;
BEGIN
OPEN CEMPLEADO;
LOOP
FETCH CEMPLEADO INTO REGISTRO;
EXIT WHEN CEMPLEADO%NOTFOUND;
END LOOP;
CLOSE CEMPLEADO;
RETURN REGISTRO.NOMBRE;
END JEFE_NIF ;
/


